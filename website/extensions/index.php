<p>WIKINDX uses a plug-in module system to extend or alter its capabilities. Additionally, there exist third-party plug-ins and extensions. All extras can be downloaded from the plugins section of the Sourceforge file downloads area. Simply extract them (with their directory structure) to the plugins/ directory to make them instantly available. Plugin options can be configured from within the web browser.</p>

<h2>Plugins include</h2>

<ul>
    <li>The WYSIWYG word processor – write an article, from draft through to publication with automatic citation formatting, entirely within the one software</li>
    <li>Bibutils – convert between a number of bibliographic formats in preparation for an import into WIKINDX</li>
    <li>ImportExportBib – import bibliographies into WIKINDX and export bibliographies – Endnote, RIS, BibTeX, RTF, HTML, PubMed</li>
    <li>SoundExplorer – experimental aural notification of selected resource additions</li>
    <li>ChooseLanguage – quickly select a new language</li>
    <li>Localization – easily create a new localization</li>
    <li>AdminStyle – create and edit bibliographic/citation styles</li>
    <li>Import from Amazon – what it says on the tin</li>
    <li>LocalDescription – change the front-page description depending on the user-selected localization</li>
    <li>RepairKit – fix corrupted character codes in imported data, missing rows, database integrity, and other fixes. The Swiss Army Knife for WIKINDX . . .</li>
    <li>BackupMysql – back up the WIKINDX</li>
</ul>

<h2>Localizations and bibliographic styles</h2>

<p>There are a variety of localizations (languages) and bibliographic/citation styles available from the sourceforge site. They act like plug-ins so simply extract them (with their directory structure) to the languages/ directory or styles/bibliography/ directory to make them instantly available.</p>