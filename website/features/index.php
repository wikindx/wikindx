<p>Listed below are the core features available by default. Functionality can be extended with plug-ins and other add-ins. See the <a href="index.php?page=extensions">Extensions page</a>.</p>

<h2>User</h2>

<ul>
    <li>41 resource types.</li>
    <li>Multi-user mode – create and manage your own bibliographies drawn from the WIKINDX master bibliography and browse other users' bibliographies. (Must be enabled by the administrator.)</li>
    <li>Create user groups and user group bibliographies. (Must be enabled by the administrator).</li>
    <li>Save your own preferences.</li>
    <li>Enter/edit bibliographic resources.</li>
    <li>Add unlimited file attachments to each resource. (Must be enabled by the administrator.)</li>
    <li>Catalogue resources by categories, sub-categories and keyword(s).</li>
    <li>Enter/edit a general note about the whole resource.</li>
    <li>Enter/edit quotes and paraphrases from those resources.</li>
    <li>Enter/edit thoughts or musings on various aspects of a resource (can be private or public).</li>
    <li>Enter/edit ideas that are independent of resources.</li>
    <li>Add keywords to resource metadata such as quotes, paraphrases and musings.</li>
    <li>Cross-reference other WIKINDX resources from within quotes, paraphrases, musings, ideas, notes and abstracts.</li>
    <li>Edit keywords, creators, publishers and journals.</li>
    <li>Comprehensive search across all the above with highlighting of search terms using either Quick Search or the flexible Advanced Search.</li>
    <li>Reorder bibliographic lists by first creator, title, resource type, publisher, year of publication or timestamp.</li>
    <li>Browse all creators, publishers, collections, categories and keywords with font colour and size indicating frequency of occurrence.</li>
    <li>Unlimited primary creators, editors, translators and revisers, composers, agents, performers etc.</li>
    <li>Export bibliographic lists (optionally annotated) with a range of formatting options to Rich Text Format [RTF] files for easy insert into word processors.</li>
    <li>Cut 'n' Paste BibTeX entries to the database (amount limited by the administrator).</li>
    <li>View and export in various bibliographic styles including Chicago, MLA, APA, Harvard, Turabian, British Medical Journal and IEEE.</li>
    <li>Run WIKINDX in core English or other languages (depending on administrator-installed language plug-ins).</li>
    <li>User-defined paging of long bibliographic lists.</li>
    <li>View all resources, quotes, paraphrases and musings or a single random one.</li>
    <li>One-click return to last bibliographic list or single view.</li>
    <li>Store up to 20 bookmarks for quick return to single views and resource lists.</li>
    <li>Add resources to a temporary basket for viewing and exporting.</li>
    <li>Select a visual style.</li>
    <li>Import WIKINDX resources into Zotero.</li>
    <li>And much, much more...</li>
</ul>

<h2>Administrator</h2>
<ul>
    <li>Design your own CSS templates for web browser display and integrate WIKINDX into your existing web site.</li>
    <li>Enable/disable multi-user mode, add users or allow user self-registration.</li>
    <li>Several levels of write and read only access.</li>
    <li>Delete resources.</li>
    <li>Add custom text fields to resources.</li>
    <li>Manage users and resource categories and sub-categories.</li>
    <li>Merge keywords into one keyword.</li>
    <li>Merge and group creators into one creator.</li>
    <li>Importation of BibTeX (.bib) bibliographies.</li>
    <li>Manage plug-ins, visual templates and localizations.</li>
    <li>Add general news items and optionally email the item to registered users.</li>
    <li>Optionally receive email notification of user registrations.</li>
    <li>Install non-English language localizations.</li>
    <li>Provide a RSS feed of latest additions and edits.</li>
    <li>WIKINDX resources can be optionally indexed by Google Scholar.</li>
    <li>WIKINDX has an architecture enabling the easy writing of plug-ins to expand its features.</li>
    <li>User registration requests implement anti-spam/bot measures.</li>
    <li>Attachments can be embargoed (blocked from public view) until a specified date – the embargo is automatically lifted on or after the specified date. Until that time, only admins can view embargoed attachments.</li>
    <li>And much, much more...</li>
</ul>